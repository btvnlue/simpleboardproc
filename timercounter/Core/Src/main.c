#define TX_BUF_SIZE 3
int txbusy = 0;
unsigned char txbuf[TX_BUF_SIZE];
int txbufpos = 0;
int txpos = 0;
int buftxsize = 0;

int __io_putchar_dma(int ch)
{
	txbuf[txbufpos] = (uint8_t)ch;
	txbufpos++;
	if (txbufpos >= TX_BUF_SIZE) {
		while (txbusy);
	}

	if (txbusy == 0) {
		buftxsize = txbufpos - txpos;
		if (buftxsize>0) {
			txbusy = 1;
			HAL_UART_Transmit_DMA(&huart2, (uint8_t*) &txbuf[txpos], buftxsize);
		}
	}

	return ch;
}

int __io_putchar(int ch) {
	__io_putchar_dma(ch);

	return ch;
}
void uart_tx_dma_bufm_cb(UART_HandleTypeDef *huart)
{
	txpos += buftxsize;
	if (txpos >= TX_BUF_SIZE) {
		txpos = 0;
		txbufpos = 0;
		txbusy = 0;
	} else {
		if (txbufpos > txpos) {
			buftxsize = txbufpos - txpos;
			HAL_UART_Transmit_DMA(&huart2, (uint8_t*) &txbuf[txpos], buftxsize);
		} else {
			txbusy = 0;
		}
	}
}

void HAL_UART_TxCpltCallback(UART_HandleTypeDef *huart)
{
	uart_tx_dma_bufm_cb(huart);
}

